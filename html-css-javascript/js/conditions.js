// Conditions (เงื่อนไข)

let score = 70;


// If-else
if (score == 50) {
    console.log('score is 50');
} else if (score > 60) {
    console.log('score greater than 50');
} else {
    console.log('score is not 50');
}


// Switch
let name = "gothen";

switch (name) {
  case "Goku":
    console.log("name is goku");
    break;

  case "Goten":
    console.log("name is goten");
    break;

  default:
    console.log("name is gohan");
    break;
}

